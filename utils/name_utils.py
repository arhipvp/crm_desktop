"""Вспомогательные функции для работы с ФИО."""

def extract_surname(full_name: str) -> str:
    """Вернуть предполагаемую фамилию из полного имени.

    Берёт первое слово из строки, предварительно обрезая пробелы. Если
    имени нет, возвращается пустая строка.
    """
    if not full_name:
        return ""
    return full_name.strip().split()[0]
